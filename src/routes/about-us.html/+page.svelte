		<p>Following the trend of real estate offices giving up store fronts and office space, Shore Realty emerged in 2006 as an  online virtual real estate company. Serving the real estate needs of   Outer Banks property owners and potential owners including the northern beaches of Corolla, Duck, and Southern Shores to the middles beaches of Kitty Hawk, Kill Devil Hills, Nags Head, the islands of Manteo and Colington and then all the way down to Hatteras Island and everywhere in between.</p>
		<p>As a virtual real estate company, Shore Realty does not require clients to travel to the office to take care of their real estate needs. Instead, Shore Realty agents go the extra step and bring everything to you, the client. </p>
		<p>If you are selling your property then it is only natural for any hands on work to be conducted at the property. There is no reason why should you be inconvenienced and asked to go to your agent's office to do anything. </p>
