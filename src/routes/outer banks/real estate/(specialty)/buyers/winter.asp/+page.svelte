	<h2 align="center" id="pageName">OBX Winter Real Estate Season </h2>
	<div class="feature">
		
		<p>Winter on the Outer Banks is not the fast paced hustle and bustle most people are accustomed to in the summer. Most of the summer type retail shops are closed in the winter. Don't be surprised to find your favorite restaurant closed for a couple of weeks or months which coincide with your visit. </p>

		<p>Not much real estate gets sold in the winter on the Outer Banks. Most of the people who come here, have to travel from areas which experience bad weather and it affects their travel here. The majority of the people wait until the spring when the weather starts to get a little nicer. </p>
		<p>Shopping for real estate is fairly easy in the winter. The hotels and motels are all empty so you can come and go as you please. All of the rental houses are vacant, so if you are looking for a summer rental home, you can look at them without having to worry about vacationers not letting us in. Winter is a good time to come down and start your search. Multiple trips are usually required before a home purchase is made, so make your first trip in the winter to experience the full range of seasons on the OBX.</p>
		<p>If you decide to shop for your new property during the winter bring a variety of clothes. The temperature doesn't get very cold but sometimes it feels a lot colder than it is because of the wind, which never stops blowing. There are sunny days, where, if you are on the windy side of a house it is brutally cold while at the same time on the other side of the house, you could lay out in the sun and work on your tan. Layer your clothes so you can adjust accordingly.</p>
		<p>Dress for the cold even if it is warm outside. The insulation in the houses hold the cold  in and make some of the winterized houses very dank and cold. A jacket is good so you can take it off after we leave a house. It is pretty odd putting on a jacket to go into a house to look around. </p>

