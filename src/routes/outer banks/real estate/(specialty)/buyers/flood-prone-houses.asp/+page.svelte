	<h2 align="center" id="pageName">8 Ways To Avoid A Flood Prone House On The Outer Banks Of NC</h2>
	<div class="feature">
		
		<p>With so many new agents coming to the Outer Banks lacking the local knowledge of the area's geography it is easy to find yourself buying a home that gets  wet. Across the Outer Banks there are different types of flooding that occurs  and where a property is located will determine what type of flooding, if any,  occurs. Some areas never experience flooding while other areas are flooded  repeatedly. This guide was created is to help home buyers avoid buying a home that is prone to flooding on the on the Outer Banks. </p>
		<p>
			<strong>1. Driveway Berm</strong>
		</p>
		<p>As you approach the house from the street look to see if  there is a berm built across the front of the driveway. It might be made of  concrete or asphalt. If you have to drive over this bump to get onto the  driveway then you have just discovered one of the home owners attempts to stop  the rain running off the road and down the driveway towards the house. These drivaeway berms are very evident on Lighthouse Dr in Corolla. </p>
		<p>
			<strong>2. Does The Driveway Run Downhill  Towards The House?</strong>
		</p>
		<p>If the driveway slopes away from the road and down towards  the home, look to see where the rain water flow ends up. If the slope of the  driveway takes the water away to a neighbor&rsquo;s property then all is good. If the  driveway slope ends at the house then the rain water has to go somewhere. </p>
		<p>A series of rain events can saturate the land and cause the  rain water to stand in puddles in the low spots. The Ground Water Level is naturally  high on the OBX which contributes to the rapid saturation of the land. When  everything is saturated the rain water runs downhill and collects in the low  spots. If </p>
		<p>Walk around the yard. Do the neighboring properties slope  down to this one? If so, where does the water go once it reaches this property?  If there is nowhere for the water to go then it forms a puddle. </p>
		<p>
			<strong>3. Black Stains On Driveway And In  Yard</strong>
		</p>
		<p>Sometimes there might only be one house in the whole  neighborhood that holds rain water and you are thinking about buying it. It can  be on top of a hill and still be lower than the neighbors and collect water.</p>
		<p>Black stains on the driveway or in the yard can be evidence  of standing water. If the black stain is in the shape of a puddle then it probably  collects rain water. Standing water will get funky pretty quickly as the water dissolves  organic matter in the ground and mildew collects in the water. When the water  evaporates this leaves a black stain. You can find lots of driveways with a  black spot or entirely covered with a black hue. This is not necessarily a deal  killer. We locals have grown accustomed to walking through puddles after a  storm. It only becomes a problem when the puddles find their way inside. </p>
		<p>
			<strong>4. Water Stains on Exterior Siding</strong>
		</p>
		<p>Walk around the perimeter of the house and look at the  siding close to the ground to see if there are water stains on the siding. This  can be washed off so be sure to look at the deck pilings as these are often  overlooked in the cleanup process.</p>
		<p>
			<strong>5. Sump Pumps </strong>
		</p>
		<p>Various flood mitigation efforts worth looking for include Sump  Pumps. These look like small trash cans buried in the yard with just the lid  exposed. These might also include pieces of white pipe attached to the buried  can or just mysteriously sticking up out of the yard. This system pumps flood  waters away in an effort to keep water out of the house.</p>
		<p>
			<strong>6. French Drain Cut Into Driveway</strong>
		</p>
		<p>Sometimes you will see a French Dain cut into the driveway  in front of the house. This was put there for only one reason and that was to divert  rain water away from the house. This might work at handling a normal rainfall  but when a tropical storm dumps torrential amounts of rain this will become  overloaded.</p>
		<p>
			<strong>7. Evidence of Water Stains in House</strong>
		</p>
		<p>Evidence of past water infiltration of a house can sometimes  be found inside the house. The owners might have made an attempt to hide this  evidence but there are some places they often overlook when painting over or  clean up the water stains. Look for water stains on the walls behind the water  heater and the heat pump and the insides of closets. Look at the inside of the garage  door. During cleanup/cover-up the garage door will usually be up in the open position  and overlooked.</p>
		<p>
			<strong>8. Tide Line In Yard Or Neighborhood</strong>
		</p>
		<p>Tidelines form when debris from a storm is left standing on  the land. You can usually find tidelines along on the ocean side and the sound  side after a big storm. The tide line might not be in the yard of the home you  are looking at. Look around the neighborhood. The Tide Line might be across the  street if the tide flowed under this house. A Tide Line usually consists of  what looks like straw. This is old marsh grass that got washed out of the  marshes during the storm. This stuff moves around in big rafts blown by the wind  and eventually ends up on shore.</p>
