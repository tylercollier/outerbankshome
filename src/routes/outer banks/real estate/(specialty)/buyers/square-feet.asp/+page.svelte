	<h2 align="center" id="pageName">SQUARE  FOOTAGE NOT  AS  ADVERTISED</h2>
	<div class="feature">

		<p>Most of the time when you look at a house that you like, what you  see and feel is more important than the actual square footage. If the  square footage is important to you then we need to measure the house  before we make an offer. We always have a tape measure handy.</p>
		<p> I have been in houses that seemed way bigger than they actually were and likewise I have seen homes that were crowded even though they had a lot of square footage. How you feel in a home is the most important thing. </p>
		<p>Unfortunately, a lot of times we have to deal with agents that either don't know how to use a tape measure, are too lazy to actually take the time to measure the house or their math skills are lacking. We routinely find the square footage measurements listed in the MLS  matching exactly with the square footage numbers in the tax records. Never use the numbers in the tax records. </p>
		<p>There are many times when a house was measured for a client the square footage numbers turned out to be different than what was advertised in the MLS. So if square feet is important to you then let us know so we can break out the tape measure. </p>
		<p>There was a time  I was measuring a house for a seller. He told me I didn't need to measure the house because he had the house built and knew exactly what the square footage was. I measured the house anyway and I came up with less square footage than he told me. He pulled out the blue prints and showed me my mistake. To prove myself correct I measured the house again, only this time I had him help me. I held the dumb end of the tape and he held the end where the measurements are. I wrote down the numbers he gave me and he watched me do the math and my original numbers were correct.</p>
		<p>Seems the builder had mistakenly built his house 2 feet shorter than he was paid to.  2 feet doesn't sound like much but it was a 3 story house and it added up. So don't ever use the blue prints to figure out the square footage of a house.</p>
		<p>More recently I showed a 2800 sq ft home and when got outside Mrs. Buyer told me she didn't think the house was 2,800 sq ft. We all agreed it was smaller. The listing agent used the tax records for the sq foot number and argued with me when I questioned the size of the house. </p>
		<p>I used the aerial program on the county website to measure the house and then took a tape measure and actually measured the house. both times it was only 1800 sq ft. The seller was paying taxes on 1,000 sq ft of house that did not exist. This was when the seller found a survey that confirmed my measurements.</p>

