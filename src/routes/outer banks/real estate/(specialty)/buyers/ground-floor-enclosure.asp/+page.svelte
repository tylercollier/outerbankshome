	<h2 align="center" id="pageName"> Problems With Ground Floor Enclosures</h2>
	<div class="feature">
		
		<p>Homes on the Outer Banks do not have basements in the ground under the structure. So you have been searching through all the Outer Banks real estate for sale looking for a nice vacation rental house or a year round home. You think you found the perfect property, but what about the f. The neighborhood is perfect, there is plenty of room in the house, it has all the features you were hoping for and the price is right. What is the f going to cost. It varies from house to house.</p>
		<p> If there is a ground floor enclosure you or your real estate agent need to do some investigating. Some of the ground floor enclosures on the Outer Banks are not supposed to be there.</p>
		<p> Some of the older houses were built when the rules were different. If it was ok back then so the insurance policy for the enclosure might be grand fathered. Some times there will be reduced rates that are available to the new owner but there will be conditions. The grandfather effect goes away if 51% of the structure is destroyed.</p>
		<p> Some of the newer and older houses were enclosed underneath illegally. The owner does the job himself without permits and it is not supposed to be there. Problems can arise during the sale when a survey gets done and the insurance company doesn't like what they see.</p>
		<p> I helped someone who bought what they thought was a duplex. I was not their agent when they made the purchase, I would have pointed out that it was not a legal duplex. It is just too easy to tell.</p>
		<p> They had to remove the entire downstairs apartment. A tenant made the neighbors mad and they called the building inspector. I helped them sell what was left of their investment after the demolition was done. It is not hard to verify anything these days.</p>
		<p> Recent changes to the Federal F program, that is run by FEMA, has implications on some newer ground floor enclosures. Some areas that were initially zoned "X" were changed in 2006 to zone "AE". This means that some newer houses with living area built on the ground might now require f with expensive premiums.</p>

