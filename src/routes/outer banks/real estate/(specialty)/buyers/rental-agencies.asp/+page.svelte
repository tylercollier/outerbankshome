	<h2 align="center" id="pageName">Your New Vacation Home And Your Rental Agency </h2>
	<div class="feature">
		
		<p>So you have been coming on vacation to the Outer Banks and now you want to buy a rental house. The idea of owning an vacation house that will help pay for itself sounds appealing but you are not sure how  the rental part works.</p>
		<p> We have been doing it here for so long we take it for granted but I will try to give you the basics about owning a home that is in a rental program and rented out weekly during the summer months. Some of this will apply to some or all of the rental agencies. At the very least you should be able to ask a few more questions of your rental agency about your rental house after you read this. </p>
		<p>There are many different styles of managing a vacation rental house. Each rental department has their own characteristics and there are deferent strategies involved. </p>
		<p>
			<b>Obligations</b> - As an owner of a vacation rental house your obligation will be to cooperate with the rental company and make sure the house is kept up to the standards set out by the rental company. The rental company is obliged by law to look out for you the owner. You are their legal client and the tenants are their customers. The rental company should not penalize your rental house or you so they can keep a tenant in the program. On the same note, you have to maintain your vacation rental house in a condition so that you are not putting the vacation renters in a bad situation. Keep it clean and fresh.
		</p>
		<p>
			<b>Most Rental Income</b> - One method is to rent the house to anybody and everybody that has a credit card. This method generates the most rental income but it also generates higher maintenance bills. A good example is what can happen to a house rented to young people during spring break. There are stories that would make your head spin.
		</p>
		<p>
			<b>Owner Service</b> - The other style of management is to pass up some renters to protect the owner and their rental house. The only real way to make sure your house is not featured on MTV spring break is to NOT make the house available to rent during this time frame. I have seen parents check into a rental house and then give the keys to their highschool kids out in the parking lot.
		</p>
		<p>There are companies that will sacrifice their own income to protect their owners. These companies cater to the owner and their rental house. The rental income will be less but so are the repairs and the stress level. If you love your house more than some extra money, look for this type of management. </p>
		<p>
			<b>Commissions</b> - The rental companies typically charge from between 15% to over 25%, depending on the property being rented and the level of income it generates. The more rental income a rental house generates the less the commission percentage charged and the less rental income generated the higher the percentage of commission.
		</p>
		<p>The commission is only collected  on any rental money actually collected and usually covers a quick cleaning on check in day, accounting, advertising and general management of the property. Some rental companies have found ways to charge fees for routine duties so question everything. </p>
		<p>When you are comparing houses by their rental income it is important to know if any of the rental companies use the all inclusive style of pricing their rental cottages. Instead of charging the rental vacationer an itemized list of fees for various services such as linens and trip insurance, some rental companies are now lumping all of the fees into the weekly price for the cottage. The end result is these rental numbers will be higher than the true net to the owner with a bevy of fees being collected for the rental agency. </p>
		<p>
			<b>Cleaning </b>- Spring cleaning  is not covered by the commission.
		</p>
		<p>You will want to have your house thoroughly cleaned each year. The turnover day cleanings will not get rid of the dirt that accumulates over the course of a year. The typical turn over day cleaning is a quick dusting, vacuum and wipe. There is not enough time for the house to be cleaned thoroughly. If the house is dirty at the beginning of the rental season it will just get dirtier as the rental season goes on. </p>
		<p>Vacation renters definitely treat a house with the same respect as the owner. If the rental house is thoroughly clean and the paint is fresh they are less likely to leave a mess. It can be as simple a thing as making the kids eat the popsicle on the deck instead of in the living room. I have seen this over and over again. Keep your house clean and the paint fresh. </p>
		<p>
			<b>Maintenance</b> - Maintenance is not covered by the commission. The rental company will send a service person to your rental house but you will have to pay the maintenance bill. Some rental companies have their own maintenance departments but you should have the option to use your own maintenance people. Do no even think about doing it yourself and don't be surprised when you get a bill for changing a light bulb.
		</p>
		<p>
			<b>Usage Limits </b>- Some of the rental companies will have a limit on the amount of personal use you are allowed. This may sound strange but the rental companies need to make a certain amount of money to operate properly and if they let owners use the rental house for 8 of the 10 summer weeks they will go out of business.
		</p>
		<p>There are also tax consequences if you use the house too much. The IRS classifies vacation rental houses as second homes/investment properties. If you spend too much time in the house you lose the investment status. There are ways around this.</p>
		<p>
			<b>The Front Desk</b> - The people that man the front desk have a thankless job. They bear the burden of having to field  complaints from both the renters and the owners and they do not have the power to do anything to solve anyone problem. As an owner, if you have a problem do not take it out on the front desk. Because they answer the phone people tend to vent their problems at these people. They can not solve your problem. Save that  for management.
		</p>
		<p>Besides taking the brunt of the problem calls the front desk people are also the ones who actually take the reservations on your house. If a potential renter is on the phone trying to decide to rent your house or another one just like yours they ask the person they are talking to for an opinion. If you just blasted that person because of a management problem you might just lose a reservation to another house.</p>
		<p>If you are one of those owner who sends nice treats to the reservation people then you might be able to take a reservation away from another owner who treats the front desk people poorly. </p>
		<p>Vacation Rental By Owner There are a number of web sites on the internet that let owners list their rental houses for a small fee. If your rental company will cooperate these companies will send new renters to your house while your rental rental company takes care of the reservations and all of the other related stuff. This is another way to increase the internet exposure of your vacation rental house. </p>

