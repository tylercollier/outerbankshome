	<h2 align="center" id="pageName"> Outer Banks Beach Access Location</h2>
	<div class="feature">
		
		<p>So you are finally ready to start shopping for your own special piece of paradise. You are on a web site dedicated to Outer Banks real estate so you have picked a good place to start. It all looks so good and all of it is on the Outer Banks. So how could you go wrong? Easy.</p>

		<p>Just because you are at the beach does not mean that every location is a good one. If you are looking for a house to move into and raise your family there are some areas that are perfect and some areas that are less desirable. I am not saying there are areas that are not safe but you wouldn't want to raise your family in the same house you vacation in. Likewise, you would not want a vacation rental home in a neighborhood where people are raising families.</p>

		<p>Here are some of the location variables that you need to consider depending on what you are looking for.</p>
		<p>
			<b>Investment Rental Home </b>
		</p>
		<p>There are not public beach accesses available everywhere on the OBX. The town of Duck does not have any public beach access. This is important to know so you do not make the mistake of buying a property where you will be trespassing if you go to the beach. Examples are the subdivisions of Jaycrest and Duck Ridge Village. Neither of these subdivisions have a beach access and you will have to drive to Kitty Hawk to go to the beach. Corolla is another area with limited public beach access. If you are looking on the westside in Corolla be sure to check into the designated beach accesses. </p>
		<p>Oceanside properties are located on the east side (ocean side) of the main road that runs the length of the Outer Banks. This is where you probably stay when you come here on vacation. This is where you want to look to purchase a vacation home or a rental house. Not all vacation/rental homes were created equal either. Beach access location is the most important criteria that a renter looks for when planning a vacation so beach access should also be an important feature for buyers to consider.</p>

		<p>Not all beach accesses are legal and some are private. A brown street sign indicates a private street so a beach access located at the end of that street would also be private. A private beach access is ok if you own property in that private neighborhood. All of the beach accesses in Duck and Southern Shores and most of the beach accesses in Corolla and on Hatteras Island are private.</p>

		<p>A publicly owned beach access will have a sign with an orange ball with a seagull under it and blue under that and the acronym CAMA. This indicates a beach access that is owned by the local government and is available to anyone. Parking is available and you don't have to worry about a fence blocking your access sometime in the future.</p>

		<p>Once you have found the available beach access location you need to determine how far it is to the property you are thinking of buying. A house located 1 block from a beach access is worth more, because it will rent more, than the same identical house 3 blocks from a beach access location. </p>

		<p>Ocean side properties are where summer vacations are spent and this is what makes these areas less desirable for year round living. During the winter you will have no neighbors and during the summer you will have new neighbors every week as the vacationers come and go.</p>
		<p>
			<b>Year Round Residentail Areas </b>
		</p>
		<p>Westside properties are located on the sound side (west) of the main road. Renters do not like to cross this road to go to the beach. These homes do not make good vacation rental homes which is why they are typically less expensive. Here is where you can find year round residential neighborhoods.</p>

