	<h2 align="center" id="pageName">Home Inspections</h2>
	<div class="feature">


		<p>&nbsp;</p>
		<p>WE ENTHUSIASTICALLY ENCOURAGE ALL HOME BUYERS TO HAVE THE HOUSE  CHECKED OUT BY A LICENSED PROFESSIONAL HOME INSPECTOR. </p>
		<p>
			This inspection will  hopefully uncover any hidden mechanical or structural defects that may  exist. The Offer to Purchase has language which requires the seller to repair these defects, or lower the price by the cost of repair.
			If  the seller refuses to make good any structural or mechanical defects  then you can legally get out of the contract.
		</p>
		<p>This does not apply to  cosmetic problems or items that are obvious, i.e. carpet stains, paint,  fogged windows, etc. The only items that we can expect the seller to make good on are mechanical ( heat/air system, plumbing, electrical, etc. ) and structural items ( roof, windows, siding, foundation, etc. ) that are not performing their intended purpose. </p>
		<p> Fee structures vary amongst inspectors and sometimes  depend upon the square footage of the dwelling. It is well worth the  cost for the peace of mind!  </p>
		<p>Typically the home inspection won't find much wrong with a house. Most owners are pretty diligent at keeping up with maintenance and repairs and the home inspections come back with nothing important and a bunch of minor details that the inspector felt obligated to point out.</p>
		<p>But even the best kept house can have problems that the seller is unaware of, especially since most of the owners live somewhere else. We sold a house one time that had just had a $14,000 exterior paint job done by the seller. The house looked good to the naked eye but to the home inspector it was another story. The siding was defective and had to be replaced. The buyer received a $30,000 credit for siding repair at closing. </p>
		<p>We have seen pilings that were rotten but not where they could be seen. The inspector had to probe down below the surface of the ground to discover the rotten wood. Rotten pilings are not that big a deal if you know about them. It is a big deal if you just purchased a home and all your cash is tied up in that home. The pilings were replaced and the buyer bought the home.</p>
		<p>The most common item found in home inspections is electrical problems. GFCI outlets older than 10 years are almost always defective. The ones in your house might not be working. You will find GFCI's around sinks and on decks and patios. They are designed to protect from electrocution around water. Push the test button to make sure yours work.</p>
		<p>Another electrical problem we are starting to see more of is where the power line connects to the houses at the electric meter. The metal box the holds ht main power supply and the meter rusts out from the salty conditions here on the Outer Banks. Sometimes the rust gets so bad someone could put their hand inside and die. This repair can be costly and it is one that should not and can not be overlooked. There is too much liability to ignore potential dangerous hazards.</p>
		<p>So, while a home inspection might seem expensive if it does not uncover any problems it is still cheap peace of mind. If you decide you do not want a home inspection you will be required to sign a piece of paper that says we begged you to get one but you chose not to. </p>
		<p>We strongly recommend that every buyer have a home inspection done on the house or condo they are trying to purchase before the closing takes place. If there are problems with the house it is better to find out before too much money and time is lost. Home inspections are not very expensive and the peace of mind is priceless. Below is a list of some of the better home inspectors on the Outer Banks and some links to home inspector organizations and home inspector directories. </p>
