		<h3>Be Prepared </h3>
		<p>When you get ready to look at houses make sure you come prepared. We try to show you all of the houses available so you will need to be prepared to keep up. Wear sneakers or something comfortable for walking. Most of the houses on the outer banks are up a flight of steps before you even get to the front door so expect a workout. Closed toe shoes are a must if you want to avoid sand spurs and cactus in the yards. Bring a warm coat in the winter, even on the nice days the inside of vacant houses retains the damp cold. </p>
		<p>Bring a camera. Pictures can help you keep track of all of the houses you looked at. After looking at about 5 houses everybody starts to confuse them all  into one. You can take a picture of the MLS printout for each house so you can keep track of which is which. Pictures can also come in handy at closing. Pictures can verify that the appliances and furnishings are the same at closing as when you first saw them. Closing usually take 45 days which is plenty of time to forget the details about the house you are buying long distance. </p>



