	<div class="feature">
		
		<p>
			Spring is  the time to get your foot in the door and have all of the prepaid rental reservation money  refunded to you at closing. Learn more about how to collect a <a href="https://www.outerbankshome.com/outer banks/real estate/buyers/rental-rebate.asp" title="Rental Rebate" > RENTAL REBATE.</a> Start shopping in the early part of the spring for that rental house so you can arrange the closing of it in the late spring. You will get the advanced rental money that the seller has been collecting right before the rest of the money starts coming in. So you get a check at closing and once a month for the next 3-5 months.
		</p>
		<p>Traditionally, spring is the busiest time of the year for real estate sales on the Outer Banks. This is the time to start thinking about getting the things done to your new house  before the summer influx of tourists. All of the businesses are starting to open up for the season, there are still lots of vacancies so you can come whenever it is convenient for you. </p>
		<p>The rental season hasn't kicked into gear yet so looking at houses is very convenient. As the spring gets closer to summer there will be renters checking in and it will get progressively harder to look at properties. Looking at rental houses will soon be restricted to the 4 hour time frame between check out and check in on Saturday and Sunday only. Most rental companies won't let us look at houses when when they occupied by a vacationer. Weekday showings stop in late June. Beating the summer crowd means you will be able to achieve your goal without traffic denying you the opportunity to see everything before you make a decision. </p>

