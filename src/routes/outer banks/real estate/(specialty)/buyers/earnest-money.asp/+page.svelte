	<h2 align="center" id="pageName">Earnest Money</h2>

	<div class="feature">
		
		<p>
			When an offer is written on a piece of real estate, one of the items that needs to be addressed is the earnest money. The Merriam Webster online dictionary defines earnest <b>:</b>a serious and intent mental state &lt;a proposal made in <em>earnest</em>&gt; So the idea behind earnest money is to show the seller that you are serious about the offer and hope that the offer becomes a contract.
		</p>
		<p>The laws in North Carolina are very clear about earnest money and how it has to be handled. Earnest money is most often applied to the purchase price of the real estate and is held in a trust account.</p>
		<p>The purpose of an earnest money  deposit is to show the seller that the buyer is serious about buying the property. By law, the real estate broker who has the earnest money has 3 days to either deposit the money into a real estate trust account.</p>
		<p>The attorney will apply the earnest money to the closing. If the property does not close, the only way the real estate broker can release the earnest money from the trust account is if both the buyers and sellers agree as to how the money will be dispersed or by order of a judge.</p>
		<p>Because of the strict laws pertaining to earnest money funds, sellers should not expect to see large earnest money deposits from buyers. </p>
		<p>It is possible for a seller to cause a sale to not take place and then not release the earnest money and the buyer has to go to court to get their money back. With this in mind, buyer's are reluctant to pay the large amounts of earnest money that you might see in some other states. </p>
		<p>At Shore Realty, we try to help our buyers limit their financial exposure while satisfying the seller at the same time. </p>

