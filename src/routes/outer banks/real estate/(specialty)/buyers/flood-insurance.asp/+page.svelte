	<h2 align="center" id="pageName">Flood Zones and Flood Insurance on the Outer Banks</h2>
	<div class="feature">


		<p>Yes! Flood insurance is available and most of the homes here on the Outer Banks will need aflood insurancepolicy.</p>
		<p>All of the Outer Banks is in some type of flood zone. In fact pretty much everything east of Raleigh is in a flood zone of some sort. Don't panic, it is not as bad as it seems. There are different types of flood zones ranging from areas that flood insurance is not required (low to no premiums) to areas that are dangerously close to being swept away by the ocean (high premiums).</p>
		<p>Fortunately, on the Outer Banks all the towns conform to and enforce the construction standards required by FEMA resulting in discounted flood insurance rates for everybody here. </p>
		<p>
			<span class="style3">Update 3/24/14</span> HR 3370 was passed effectively ending the effects of the Bigert-Waters Act. <a href="https://www.outerbankshome.com/outer banks/real estate/buyers/flood insurance update 3-26-14.pdf" title="Flood Insurance Updates 3/24/14">HERE</a> is a copy of the law pointing out exactly what is and isn't regarding f.
		</p>
		<p>Some rules of thumb to keep in mind while you are looking for a house or lot are:</p>

		<h4>
			<b>Elevation</b>
		</h4>
		<p>The current regulations require that the first floor of the house to be 9 to 10 feet above sea level. If the ground is low, like most of it is on the Outer Banks, the house should be on an elevated foundation like pilings. An elevated house has minimal flood insurance premiums. </p>
		<p>If the ground elevation is 10 feet or higher the Flood Zone will probably be "X". This means a house can sit on or close to the ground and no flood insurance is required. </p>

		<h4>
			<b>Age</b>
		</h4>
		<p>While the codes are strictly enforced today, there was a time when people were allowed to build right on the ground in low lying areas. If you are looking at a house that is older and looks out of place compared to the new houses, be careful. </p>
		<p>Start asking questions about elevation and f. If the owner does not have flood insurance it could mean you won't need it either unless the current owner does not have a mortgage. Sometimes when people do not have a mortgage on a house they don't carry insurance that is required by lenders. </p>

		<p>Sometimes a house that was built too low has reasonably priced flood insurance and a new owner can acquire the same grand fathered policy at the same price. Sometimes this is not permitted by the insurance company.</p>

		<h4>
			<b>Topography</b>
		</h4>


		<p>Just because the ground level is well above sea level it does not mean that the property won't flood. If there can be lakes on the top of mountains then the shallow lot in the middle the high street can puddle in the rain. If the rain is coming from a tropical storm that puddle can get 1-3 feet deep.</p>
		<p>I have seen many houses with water standing around them after a heavy rain fall. You don't want it happening to you. To try to avoid this make sure the driveway and the neighboring properties don't slope down towards the house you are looking at. </p>

		<p>There are whole neighborhoods that become submerged from ocean over wash during some of these storms. These areas are best identified by someone with experience.</p>

		<h4>
			<b>FEMA Flood Zones </b>
		</h4>
		<p>X Zone - your lender will not require f.</p>
		<p>AE Zone - the land is below the required elevation necessary  for x zone and the property has a chance of flooding. Houses in these  zones will require flood insurance and the structure should be elevated  to obtain inexpensive f. So, if you see a house built on the ground and it is in an AE zone you  will probably want to avoid it unless the builder brought in a bunch of  fill and elevated the homesite to meet the FEMA requirements.</p>
		<p>VE Zone - wave velocity zone usually reserved for ocean front and sound front properties and some adjoining properties. </p>
		<p>COBRA Zone - in an effort to restrict dev elopement on the Outer Banks the Federal Government has created a number of parks. These parks carry the COBRA designation which means FEMA flood insurance is not available. Unfortunately, when they were drawing up the boundaries for the COBRA Zones they inadvertently overlapped some adjoining residential neighborhoods. Properties in the overlapped zones can not get federally backedflood insuranceand will require private flood insurance. </p>
