		<h1>Professional Photography </h1>
		<div class="feature">
			<p>With 80% of  home buyers using the internet to shop for real estate, the need for as many (good)  photographs as possible is one on the most important aspects of a good marketing strategy. The pictures sent to the MLS are forwarded from the MLS to all of the other websites you see on the internet. The pictures of you property will be seen everywhere and should be of the best quality available.</p>
			<p>Shore Realty uses the services of professional photographers to produce the best pictures available. Shore Realty finds this to be so important they take on the expense of paying a professional photographer to provide  professional photographs for their marketing campaign.  </p>
			<p>Sometimes, logistics get in the way with scheduling a professional's services when the property is occupied with vacationing renters. Good pictures can only be taken of the interior when their is not any luggage laying around, so pictures have to be shot on turnover day for busy rental houses. When this happens and the seller is in a hurry to get the house on the market, Shore Realty has the expertise to take good photographs inhouse. </p>
			<p>To get the  best pictures, we have invested in a wide-angle lens camera. This high-resolution  camera has a wide-angle lens which takes pictures that not only make rooms look  bigger, but it also includes more of the room in each picture. These wide-angle  pictures look great in our Virtual Floor Plans.</p>
			<p>Most buyers  will view pictures of your home before they decide to look in person, so you  can&rsquo;t risk chasing them away before they get here with low-quality pictures!  The more buyers we can get inside your property, the more it will likely sell  for.</p>
			<p>Too many  agents are still using poor-quality cameras&hellip;or their cell phones&hellip;to take photographs  of one of the biggest investments of your life. Pictures sell houses, so don&rsquo;t  trust your investment with someone who hasn't spent the time or money obtaining  equipment necessary to properly market your property.</p>
			<p>There is no  going back.&nbsp; Buyers shop online  first.&nbsp; Pictures sell houses.</p>
