		<h1>Due Diligence </h1>
		<div class="feature">

			<p>
				<br />
				The NC Offer To Purchase and Contract now uses a Due  Diligence Clause to replace the old Loan Contingency and Home Inspection  period. Previously, a buyer would have a short time frame in which to conduct  their home inspection through a 'Home Inspection Contingency' and a longer time  frame to work through the loan process through a 'Loan Contingency'. If the  buyer failed to finish either task during the specified time frame they would  lose their right to get their 'Earnest Money' deposit back. If the buyer did  not like the results of the home inspection they could back out of the contract  and have their earnest money refunded back to them during the time frame laid  out in the Home Inspection Clause section of the contract.
			</p>
			<p>    Likewise, if the buyer failed to obtain their loan, they  could back of the contract and get their earnest money back as long as they did  so during the Loan Contingency time frame.</p>
			<p>    This has all been replaced with the Due Diligence section  of the new contract. The sets up a timeframe for the buyer to perform their due  diligence. During this period, the potential buyer can do whatever they feel  necessary to reach a point where they are comfortable going through with the  purchase. Typically, this entails getting a home inspection performed and a  home mortgage set up. The home inspection takes 2 or 3 weeks to get done and  any repairs negotiated. </p>
			<p>
				Unfortunately, the Due Diligence also replaces the <a href="https://www.outerbankshome.com/outer banks/real estate/sellers/loan-contingency.html" title="Loan Contingency">Loan  Contingency</a>. The loan process typically needs 40-45 days to get the appraisal  and loan documentation completed. To protect their buyers, agents will write up  the due diligence period with a long enough time frame to allow their buyers to  jump through all the hoops necessary to get a loan. You can expect to see the buyer of your property ask for a 40 day  Due Diligence Period in order to have enough time to get their loan.
			</p>
			<p>
				This Due Diligence Clause puts a lot of power in the  buyers hands. During the Due Diligence Period, the buyer can walk away from the  contract without giving any reason and get their <a href="https://www.outerbankshome.com/outer banks/real estate/sellers/earnest-money.html" title="earnest money">Earnest Money</a> returned to  them. So, if you sign a contract for the sale of your house with a 40 day due  diligence period, the buyer will be free to change their mind for any or no  reason during those 40 days and get their earnest money back.
			</p>
			<p>    To protect the sellers represented by Shore Realty, we  rewrite these contracts eliminating the 40 day due diligence period with a  shorter time frame. Here is an article about the Loan Contingency that replaces the long due diligence period. This shorter time frame allows the buyer to get their home inspection completed, the loan contingency gives them enough time to get a loan and the can't walk away from the deal a couple of days before closing. </p>
			<p>&nbsp;</p>
