		<h1>Out of State Sellers</h1>
		<div class="feature">
			<p>
				Are you thinking about selling your property on the <a href="https://www.outerbankshome.com/" title="Outer Banks">Outer Banks</a> but have concerns because  you live out of state? Don't worry, so do most of the other home sellers on the Outer Banks. It can be a daunting task for out of state sellers if you do not have the right expertise helping you in the process. Fortunately, we have a system set up to make it as painless as possible for those residing out of state. Sellers can count on our experience.
			</p>


			<h3>Contracts and Documentation</h3>
			<p>The listing agreement can be  signed electronically, faxed, emailed or sent through the regular mail to you for your signatures. The same thing goes for the offer to purchase that comes from the buyers and any other paperwork (home inspection, addendums, repair agreement, bill of sale, etc.) needed to complete the transaction. You choose whatever method is most convenient for you to send and receive documents. </p>
			<p> We have an online fax system the can receive faxes or emails and send them as emails or faxes. Our fax number is toll free so you can borrow a friends fax machine and not worry about the long distance charges. </p>

			<h3>Closing</h3>
			<p>Everything can be handled from the comfort of your home, even the final closing. Real estate closings in North Carolina are handled by attorneys. Typically, the sellers have already signed everything except the new deed,  long before the actual closing. At the time of the closing the only thing required of the seller is to  sign the new deed, either in front of the closing attorney or in front of a Notary Public. It is cheaper for you to have the  attorney  send you all of the necessary paper work by overnight delivery than it is for you to drive down here to sign a pile of papers. There are Notary Publics everywhere and overnight delivery  is cheaper than gas. </p>
			<p>The only reason you might have to come back down is to empty the owners lockup and remove any personal items that do not go with the house. If the closing day is not convenient for your schedule you can come down anytime that is convenient and get your stuff. If you want, you can meet with the attorney at that time and sign the paperwork and let the attorney hold it until the actual day of closing or let the attorney overnight the paperwork to you.</p>
			<p> If a problem  arises at the last minute you can count on us to solve it. Our experience will allow you the comfort of worry-free selling from your home. </p>

			<h3>Sellers Choice</h3>
			<p>It is your choice. You can come down to closing and sign the deed in front of the attorney and go home with a bank check or you can stay home and have your money delivered to you or wired to your account. Long distance transactions have become so much easier with all the new technology available. Plan your last visit around your schedule and not around the schedule of the closing attorney.</p>
			<p>At this stage of the process, there are sellers who never see their house again. </p>
		</div>

