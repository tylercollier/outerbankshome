		<h1>Reverse Offer</h1>
		<div class="feature">
			<p>This is a  selling technique being touted as the new solution to a slow real estate market. The idea is for the seller to make a reverse offer to everyone who has looked at the house. After a showing, the listing agent working for the seller of the property calls up the buyer's agent and tells them the seller is willing to sell the house to them at a discounted price. This is a reverse offer. </p>
			<p>Sound confusing? Well it is, and whether it works or not is open to debate. Following are some of the pros and cons of a reverse offer. </p>
			<p>The only way the listing agent can offer a discount price to the buyer is if the property is priced high enough so that there is room to do it. For those sellers who are interested in testing the market to see if they can get their dream price for the house, this technique sounds pretty good. List high and call the buyers to generate an offer. </p>
			<p>
				For the <a href="https://www.outerbankshome.com/outer banks/real estate/sellers/" title="Sellers">sellers</a> who want to sell their property for the highest price the current real estate market will bear, a reverse offer poses a problem--by overpricing the house you will limit the number of buyers who see it.
			</p>
			<p>Buyers these days can shop real estate all over the internet so they have become educated about the pricing of houses in the neighborhoods where they want to be. They know what houses they want to look at. </p>
			<p>The more buyers you have look at a property, the better the chance of getting more money for the house or even multiple offers. It has been proven that a house that doesn't get shown much sells for less than the same house with a lot of buyer traffic.  Overpriced houses do not get shown as much as houses priced how the real estate market dictates at the time. </p>
			<p>So now the seller finally has a looker and the listing agent calls them up with a discounted price. What the interested buyers hear coming from the listing agent is that the seller is either desperate, struggling, getting divorced, sick, in financial trouble or needs to sell and is willing to negotiate a real bargain of a deal. Not the ideal position to be in for a seller to be starting the negotiation process but a dream scenario for a buyer. </p>
			<p>Our experience is these types deals end up costing the seller money. It is almost like the seller is giving permission to the listing agent to reveal the seller's bottom line to anyone and everyone who comes by.</p>
