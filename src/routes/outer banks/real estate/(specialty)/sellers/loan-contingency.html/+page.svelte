		<h1>Loan Contingency </h1>
		<div class="feature">

			<p>
				<br />
				To better understand this article you should read this  article on <a href="https://www.outerbankshome.com/outer banks/real estate/sellers/due-diligence.html" title="Due Diligence">Due Diligence</a> first.
			</p>
			<p>    One thing is certain, the offer we will receive on your  property will have a long due diligence period. The new NC contract uses the  Due Diligence clause, instead of a loan contingency, as a way to protect the  buyer in case there is an unforeseen problem with getting the loan. Because of  this deficiency in the contract, buyer agents are all writing offers with long  Due Diligence periods.</p>
			<p>    This is not because the buyer's borrowing ability is  questionable. The loan might not get funded for many reasons outside the  control of the buyer, such as the house does not appraise for the contract  price, interest rates suddenly skyrocket pushing the cost out of the buyer's  reach or their lender made promises they cannot keep. For these reasons, the buyer does need some protection during the loan  process which usually takes 40+- days.</p>
			<p>    Unfortunately, the 40 day Due Diligence period gives the buyer  the option to walk away from the deal for any reason during this timeframe. So, while they are working on their loan they can change their mind for any reason at all. </p>
			<p> This is unacceptable and if you list your property with  Shore Realty you will not have to deal this inequity. There is no reason why a buyer should have the ability to walk away from the purchase deal for any reason for such long period of time. To protect our sellers, Shore Realty has  developed a system where we reinstate the use of a loan contingency. Here is  how it works.</p>
			<p>
				The buyer's offer comes to us with the expected long Due Diligence  period.
				We make a counter offer back to the buyer where we  shorten the Due Diligence period to 15 days for the home inspection and include a Loan Contingency that gives the buyer the 40+- days they need to get their loan.
			</p>
			<p>The 40 day Loan Contingency  gives the buyers protection during the loan process but it eliminates their ability to cancel the deal for any reason. The Loan Contingency stipulates that the only valid reason for canceling the contract is if the loan is not approved for some reason outside the buyer's control. </p>
			<p>
				You, the seller, can now start making plans on how to  proceed after closing instead of wondering whether the buyer might arbitrarily change their  mind. Loans rarely fail to go through but people change their minds all the  time.
			</p>
