		<h2 align="center" id="pageName">Absorption Rate </h2>
		<div class="feature">
			<p>If you know the absorption rate for your property then you will have a good idea of how long it will take to sell your property. There are only so many buyers at any given time for any product. If you look back at the history of the real estate market and see that for the past 2 years, 100 homes were sold every month in your neighborhood, it would be safe to say 100 homes will sell next month. There are always variables and outside influences but the trends exist. With a strong history of 100 units per month sales you can extrapolate and say that 1200 homes will sell  in your neighborhood in the next year, 100 x 12 months. If there are currently 1800 homes for sale then it will take 1.5 years to sell off the inventory. </p>
			<p>If you have a time frame then looking at the absorption rate will help you determine the best strategy to accomplish your goals. If there is 1.5 years worth of inventory for sale and you need to sell in the next 6 months then you need to be priced to compete with the bottom 33% lowest priced properties. </p>
			<p>
				The above numbers are fantasy. If you would like to know what the absorption rate is for your OBX neighborhood call us at <a href="tel://8006471868">800-647-1868</a>.
