	<p>
		If you find yourself driving north on Rte 12 on the Outer Banks and the road abruptly ends on
		the beach you have found The Villages at Ocean Hill. This upscale ocean to sound community
		boasts 2 community swimming pools. One pool is on the ocean front and the other pool is located
		soundside next to Crystal Lake. Other amenities include 4 tennis courts, a basket ball court,
		nature trails and a fitness center.
	</p>
	<p>
		The Villages at Ocean Hill is one of the few communities on the Outer Banks with central sewage.
		The NC Estuarine Research Reserve is located at the "S" turn in the middle of the Villages at
		Ocean Hill. The area north of the Reserve is in an are designated as a COBRA Zone by FEMA and
		there are restrictions on <a
			href="https://www.outerbankshome.com/outer banks/real estate/buyers/flood-insurance.asp"
			title="flood insurance">flood insurance</a
		>.
